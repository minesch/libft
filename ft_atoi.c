int	ft_atoi(const char *str)
{
	int	res;
	int	mop;

	mop = 1;
	res = 0;
	while ((*str == 32) || (*str >= 9 && *str <= 13))
		str++;
	if ((str[0] == '-') || (str[0] == '+'))
	{
		if (*str == '-')
			mop *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		res = (res * 10) + ((int)*str - '0');
		str++;
	}
	return (res * mop);
}
